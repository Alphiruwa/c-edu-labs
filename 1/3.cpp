#include <iostream>
#include <clocale>

void main(void)
{
	setlocale(LC_ALL, "");
	
    int a = 2, b = 5;

    // Объявления функций (прототипы)
    void obmen1(int, int);        // передача по значению
    void obmen2(int*, int*);      // передача по указателю
    void obmen3(int&, int&);      // передача по ссылке

    std::cout << "до обмена: a=" << a << " b=" << b << std::endl;

    // Вызов obmen1 - передача по значению
    obmen1(a, b);
    // После этого вызова значения a и b НЕ изменятся, так как функция
    // работала с копиями переменных
    std::cout << "после обмена 1: a=" << a << " b=" << b << std::endl;

    // Вызов obmen2 - передача по указателю
    obmen2(&a, &b);
    // Передаем АДРЕСА переменных a и b (& - оператор взятия адреса)
    // После этого вызова значения a и b изменятся, так как функция
    // работает непосредственно с ячейками памяти переменных
    std::cout << "после обмена 2: a=" << a << " b=" << b << std::endl;

    // Вызов obmen3 - передача по ссылке
    obmen3(a, b);
    // Передаем ССЫЛКИ на переменные a и b
    // После этого вызова значения a и b снова изменятся, так как ссылки
    // являются псевдонимами оригинальных переменных
    std::cout << "после обмена 3: a=" << a << " b=" << b << std::endl;
}

// ВАРИАНТ 1: Передача параметров по значению
void obmen1(int x, int y)
{
    // x и y - это КОПИИ переменных a и b
    // Все операции происходят только с копиями

    int temp = x;   // Сохраняем значение копии x во временную переменную
    x = y;          // Присваиваем копии x значение копии y
    y = temp;       // Присваиваем копии y сохраненное значение из temp

    // В этот момент:
    // - x содержит исходное значение y
    // - y содержит исходное значение x
    // - оригинальные переменные a и b НЕ ИЗМЕНИЛИСЬ
    // - обмен произошел только внутри функции
}

// ВАРИАНТ 2: Передача параметров по указателю
void obmen2(int* x, int* y)
{
    // x и y - это УКАЗАТЕЛИ на переменные a и b
    // x содержит адрес переменной a, y содержит адрес переменной b
    // *x - разыменование указателя (получение значения по адресу)

    int temp = *x;  // Сохраняем ЗНАЧЕНИЕ, находящееся по адресу x (значение a)
    *x = *y;        // Присваиваем ЗНАЧЕНИЕ по адресу x значением по адресу y
    *y = temp;      // Присваиваем ЗНАЧЕНИЕ по адресу y сохраненным значением

    // В этот момент:
    // - переменная a (по адресу x) содержит исходное значение b
    // - переменная b (по адресу y) содержит исходное значение a
    // - оригинальные переменные a и b ИЗМЕНИЛИСЬ
    // - обмен произошел успешно
}

// ВАРИАНТ 3: Передача параметров по ссылке
void obmen3(int& x, int& y)
{
    // x и y - это ССЫЛКИ на переменные a и b
    // Ссылки являются псевдонимами (альтернативными именами) оригинальных переменных
    // Работа с x и y эквивалентна работе непосредственно с a и b

    int temp = x;   // Сохраняем значение переменной a (через ссылку x)
    x = y;          // Присваиваем переменной a значение переменной b
    y = temp;       // Присваиваем переменной b сохраненное значение

    // В этот момент:
    // - переменная a (через ссылку x) содержит исходное значение b
    // - переменная b (через ссылку y) содержит исходное значение a
    // - оригинальные переменные a и b ИЗМЕНИЛИСЬ
    // - обмен произошел успешно
    // - синтаксис наиболее простой и читаемый
}
