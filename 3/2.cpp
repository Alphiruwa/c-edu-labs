#include <clocale>

// Функция пузырьковой сортировки (Bubble Sort)
void bubbleSort(int arr[], int n) {
    for (int i = 0; i < n - 1; i++) {
        // Оптимизация: флаг для отслеживания, были ли обмены
        bool swapped = false;

        for (int j = 0; j < n - i - 1; j++) {
            // Если текущий элемент больше следующего, меняем их местами
            if (arr[j] > arr[j + 1]) {
                // Обмен элементов
                int temp = arr[j];
                arr[j] = arr[j + 1];
                arr[j + 1] = temp;
                swapped = true;
            }
        }

        // Если не было обменов, массив уже отсортирован
        if (!swapped) {
            break;
        }
    }
}

// Функция быстрой сортировки (Quick Sort)
void quickSort(int arr[], int left, int right) {
    int i = left;      // Левый указатель (начало текущего сегмента)
    int j = right;     // Правый указатель (конец текущего сегмента)
    int pivot = arr[(left + right) / 2];  // Опорный элемент (середина массива)

    // Разделение массива относительно опорного элемента
    while (i <= j) {
        // Поиск элемента слева, который больше или равен опорному
        while (arr[i] < pivot)
            i++;

        // Поиск элемента справа, который меньше или равен опорному
        while (arr[j] > pivot)
            j--;

        // Если нашли элементы не на своих местах - меняем их местами
        if (i <= j) {
            // Обмен элементов
            int temp = arr[i];
            arr[i] = arr[j];
            arr[j] = temp;
            i++;
            j--;
        }
    }

    // Рекурсивная сортировка левой и правой частей
    if (left < j)
        quickSort(arr, left, j);    // Сортировка левой части
    if (i < right)
        quickSort(arr, i, right);   // Сортировка правой части
}

int main(void) {
	setlocale(LC_ALL, "");
	
    // Инициализация исходного массива с произвольными значениями
    int mas[] = { 2, 5, -8, 1, -4, 6, 3, -5, -9, 13, 0, 4, 9 };

    // Вычисление количества элементов в массиве
    int n = sizeof(mas) / sizeof(int);

    // Очистка экрана
    std::cout << "\033[2J" << std::flush;

    // Вывод исходного (неотсортированного) массива на экран
    std::cout << "Исходный массив: ";
    for (int i = 0; i < n; i++)
        std::cout << mas[i] << ' ';
    std::cout << std::endl;

    // Создаем динамическую копию массива для пузырьковой сортировки
    int* mas2 = new int[n];
    for (int i = 0; i < n; i++) {
        mas2[i] = mas[i];
    }

    // Вызов функции быстрой сортировки для всего массива
    quickSort(mas, 0, n - 1);

    // Вывод массива после быстрой сортировки
    std::cout << "После быстрой сортировки: ";
    for (int i = 0; i < n; i++)
        std::cout << mas[i] << ' ';
    std::cout << std::endl;

    // Вызов функции пузырьковой сортировки
    bubbleSort(mas2, n);

    // Вывод массива после пузырьковой сортировки
    std::cout << "После пузырьковой сортировки: ";
    for (int i = 0; i < n; i++)
        std::cout << mas2[i] << ' ';
    std::cout << std::endl;

    // Освобождаем динамическую память
    delete[] mas2;

    return 0;
}