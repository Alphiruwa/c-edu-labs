// Для double используем %f так как это тип с плавающей точкой
#define PR(x) printf("x=%u, *x=%f, &x=%u\n",x,*x,&x)

#include <stdio.h>

void main(void)
{
	// Объявление массива типа double (числа с плавающей точкой двойной точности, 8 байт)
	double mas[] = { 100.5, 200.5, 300.5 };

	// Объявление двух указателей на double
	double* ptr1, * ptr2;

	// ptr1 присваивается адрес начала массива
	ptr1 = mas;

	// ptr2 присваивается адрес третьего элемента массива
	ptr2 = &mas[2];

	// Вывод информации о ptr1 ДО инкремента:
	// - x: адрес mas[0] в памяти
	// - *x: значение 100.5
	// - &x: адрес переменной ptr1
	PR(ptr1);

	// Увеличиваем ptr1 на 1
	// Для double это увеличивает адрес на 8 байт (sizeof(double))
	// ptr1 теперь указывает на mas[1]
	ptr1++;

	// Вывод информации о ptr1 ПОСЛЕ инкремента:
	// Теперь показывает адрес mas[1] и значение 200.5
	PR(ptr1);

	// Вывод информации о ptr2:
	// ptr2 указывает на mas[2] со значением 300.5
	PR(ptr2);

	// Увеличиваем ptr2 на 1
	// Адрес увеличивается на 8 байт, теперь ptr2 указывает за пределы массива
	++ptr2;

	// Вычитание указателей: разница в элементах double
	// Результат показывает, сколько элементов double помещается между указателями
	printf("ptr2-ptr1=%u\n", ptr2 - ptr1);
}
