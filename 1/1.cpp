#include <iostream>  // Подключаем библиотеку для ввода/вывода
#include <clocale>
// Главная функция программы
void main(void)
{
	setlocale(LC_ALL, "");
    // Объявление переменных:
    int base, i, k;           // base - основание степени, i - счетчик, k - результат
    int power(int, int);       // Прототип (объявление) функции power
    // Вывод приглашения для пользователя
    std::cout << "Введите основание степени: ";
    // Чтение введенного пользователем значения в переменную base
    std::cin >> base;
    // Цикл for для вычисления степеней от 0 до 9
    for (i = 0; i < 10; i++)
    {
        // Вызов функции power для вычисления base в степени i
        k = power(base, i);
        // Вывод результата на экран
        std::cout << "i = " << i << ", k = " << k << std::endl;
    }
}
// Функция для возведения числа в степень
// Принимает: x - основание, n - показатель степени
// Возвращает: x в степени n
int power(int x, int n)
{
    int i, p = 1;  // i - счетчик цикла, p - аккумулятор результата (инициализирован 1)
    // Цикл для умножения основания на себя n раз
    for (i = 1; i <= n; i++)
        p *= x;  // Умножаем текущий результат на основание
    return p;  // Возвращаем полученный результат
}
